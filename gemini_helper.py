def analyze_food_photo(image_bytes: bytes, mime_type: str = "image/jpeg", caption: str = None) -> dict | None:
    try:
        prompt = PHOTO_SYSTEM_PROMPT
        if caption:
            prompt = f"{prompt}\n\n–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å —É—Ç–æ—á–Ω–∏–ª: {caption}\n–û—Ü–µ–Ω–∏ –ø–æ—Ä—Ü–∏—é –∏ –ö–ë–ñ–£ —Å —É—á—ë—Ç–æ–º —ç—Ç–æ–≥–æ —É—Ç–æ—á–Ω–µ–Ω–∏—è. –í name –ø–æ-–ø—Ä–µ–∂–Ω–µ–º—É —É–∫–∞–∂–∏ –∫–æ–º–ø–æ–Ω–µ–Ω—Ç—ã —Å –≥—Ä–∞–º–º–∞–º–∏."
        response = model.generate_content([
            prompt,
            {"mime_type": mime_type, "data": image_bytes}
        ])
        text = response.text.strip()
        text = re.sub(r"```json|```", "", text).strip()
        return json.loads(text)
    except Exception as e:
        print(f"Gemini photo error: {e}")
        return None

